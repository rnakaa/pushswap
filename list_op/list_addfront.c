#include"../push_swap.h"

void	add_front(t_stack **a, t_stack *stack)
{
	if (!*a)
	{
		(*a) = stack;
		return ;
	}
	(*a)->prev = stack;
	stack->next = *a;
	(*a) = stack;
}
